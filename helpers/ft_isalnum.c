#include "libft.h"

static int	alpha(int a)
{
	if ((a >= 'a' && a <= 'z') || (a >= 'A' && a <= 'Z'))
	{
		return (1);
	}
	return (0);
}

int	ft_isalnum(int n)
{
	if (alpha(n) || (n >= '0' && n <= '9'))
		return (1);
	return (0);
}
