#include "libft.h"

char	*ft_strcat(char *d, char *s)
{
	int	i;
	int	x;

	i = 0;
	while (d[i])
		i++;
	x = 0;
	while (s[x])
	{
		d[i] = s[x];
		i++;
		x++;
	}
	d[i] = '\0';
	return (d);
}
